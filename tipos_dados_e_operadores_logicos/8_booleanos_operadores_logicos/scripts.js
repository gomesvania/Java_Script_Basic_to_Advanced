// Booleanos
// true ou false
console.log(3 > 100 && 10 > 5); // false
console.log(3 < 100 || 10 < 5); // true

console.log(3 == 3 && 'Vania' == 'Vania'); // true
console.log(3 == 3 && 'Vania' == 'Vania' && 10 != 7); // true

console.log("Marta" == "Marta" || false); // true
console.log("Victor" == "Marcelo" || 10 < 5); // false
console.log(false || false); // false

console.log(!true); // false
console.log(!false); // true
console.log(!true && true); // false



